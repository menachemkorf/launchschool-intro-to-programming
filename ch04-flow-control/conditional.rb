# Example 2
if x == 3
  puts "x is 3"
elsif x == 4
  puts "x is 4"
end

# Example 3
if x == 3
  puts "x is 3"
else
  puts "x is NOT 3"
end

# Example 4
if x == 3 then puts "x is 3" end
  
# Example 5: ternary operator
puts x == 3 ?  "x is 3" :  "x is not 3"